package POJO;

public class Product
{
  private long id;
  private long price;
  private long strokeId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getPrice() {
    return price;
  }

  public void setPrice(long price) {
    this.price = price;
  }


  public long getStrokeId() {
    return strokeId;
  }

  public void setStrokeId(long strokeId) {
    this.strokeId = strokeId;
  }

}
