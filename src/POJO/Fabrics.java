package POJO;


public class Fabrics
{
  private long id;
  private String name;
  private String color;
  private long width;
  private long height;
  private long price;
  private long productId;
  private long productStrokeId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getColor() {
    return color;
  }

  public void setColor(String color) {
    this.color = color;
  }


  public long getWidth() {
    return width;
  }

  public void setWidth(long width) {
    this.width = width;
  }


  public long getHeight() {
    return height;
  }

  public void setHeight(long height) {
    this.height = height;
  }


  public long getPrice() {
    return price;
  }

  public void setPrice(long price) {
    this.price = price;
  }


  public long getProductId() {
    return productId;
  }

  public void setProductId(long productId) {
    this.productId = productId;
  }


  public long getProductStrokeId() {
    return productStrokeId;
  }

  public void setProductStrokeId(long productStrokeId) {
    this.productStrokeId = productStrokeId;
  }

}
